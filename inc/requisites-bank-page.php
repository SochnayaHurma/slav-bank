function sb_build_block_markup(string $name, array $attrs = [], string $inner = ''): string
{
    $json = !empty($attrs)
        ? ' ' . wp_json_encode($attrs, JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES)
        : '';

    if ($inner === '') {
        return sprintf('<!-- wp:%1$s%2$s /-->', $name, $json);
    }

    return sprintf(
        "<!-- wp:%1\$s%2\$s -->\n%3\$s\n<!-- /wp:%1\$s -->",
        $name,
        $json,
        $inner
    );
}

