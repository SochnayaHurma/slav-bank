function sb_get_requisites_bank_page_markup(): string
{
    $hero_markup = sb_build_block_markup(
        'slavbank/page-hero-split',
        sb_get_requisites_bank_hero_attrs()
    );

    $shell_inner = sb_build_block_markup(
        'slavbank/html-sections-card',
        [
            'sections' => sb_get_requisites_bank_sections(),
        ]
    );

    $shell_markup = sb_build_block_markup(
        'slavbank/page-shell-stack',
        [
            'anchorId' => 'key',
            'showAlert' => false,
        ],
        $shell_inner
    );

    $table_markup = sb_build_block_markup(
        'slavbank/keyvalue-copy-table',
        [
            'anchorId' => 'full',
            'headerKey' => 'Параметр',
            'headerValue' => 'Значение',
            'rows' => sb_get_requisites_bank_rows(),
        ]
    );

    return implode("\n\n", [
        $hero_markup,
        $shell_markup,
        $table_markup,
    ]);
}

function sb_register_requisites_bank_pattern(): void
{
    if (!function_exists('register_block_pattern')) {
        return;
    }

    register_block_pattern(
        'slavbank/requisites-bank-page',
        [
            'title'       => 'Славянбанк / Реквизиты банка',
            'description' => 'Полная страница реквизитов банка, собранная из базовых блоков темы.',
            'categories'  => ['featured'],
            'content'     => sb_get_requisites_bank_page_markup(),
        ]
    );
}
add_action('init', 'sb_register_requisites_bank_pattern');

function sb_seed_requisites_bank_default_content(string $content, WP_Post $post): string
{
    if (!sb_is_requisites_bank_page($post)) {
        return $content;
    }

    if (trim($content) !== '') {
        return $content;
    }

    return sb_get_requisites_bank_page_markup();
}
add_filter('default_content', 'sb_seed_requisites_bank_default_content', 10, 2);