function sb_is_requisites_bank_page(WP_Post $post): bool
{
    if ($post->post_type !== 'page') {
        return false;
    }

    $slug = (string) $post->post_name;

    return in_array($slug, sb_requisites_bank_page_slugs(), true);
}

